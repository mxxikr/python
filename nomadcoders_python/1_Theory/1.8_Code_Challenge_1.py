# 계산기

def plus(a, b):
  a = int(a)
  b = int(b)
  result = a + b
  print(result)
def minus(a, b):
  a = int(a)
  b = int(b)
  result = a - b
  print(result)
def times(a, b):
  a = int(a)
  b = int(b)
  result = a * b
  print(result)
def div(a, b):
  a = int(a)
  b = int(b)
  result = a / b
  print(result)
def remi(a, b):
  a = int(a)
  b = int(b)
  result = a % b
  print(result)
def nega(a, b):
  a = int(a)
  b = int(b)
  result = -a
  print(result)
def pow(a, b):
  a = int(a)
  b = int(b)
  result = a ** b
  print(result) 
  
